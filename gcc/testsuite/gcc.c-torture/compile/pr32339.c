struct city_dialog
{
  struct city *pcity;
  char change_list_names[200][200];
  int change_list_ids[200];
};
change_callback (void)
{
  struct city_dialog *pdialog;
  int n;
  int i;
  get_city_dialog_production_full (pdialog->change_list_names[n],
    pdialog->pcity);
  pdialog->change_list_ids[n++] = i;
}
