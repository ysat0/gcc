extern void abort(void);

int main()
{
  int i;

  for (i = 0; i < sizeof(tests)/sizeof(tests[0]); ++i)
    {
      V r = __builtin_shuffle(tests[i].in, tests[i].mask);
      if (memcmp(&r, &tests[i].out, sizeof(V)) != 0)
	abort();
    }

  return 0;
}
